package designpattern1;

/**
 * Shape interface for various shapes.
 * It declares the draw method that concrete shapes must implement.
 *
 * @author anmolvalecha
 * @version 1.0
 */
public interface Shape {
     /**
     * Draws the shape and returns a string representation.
     *
     * @return A string representing the drawn shape.
     */
    String draw();
}
